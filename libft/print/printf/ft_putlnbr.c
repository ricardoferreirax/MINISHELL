#include "ft_printf.h"

void	ft_putlnbr(int nb, int *n)
{
	if (nb == -2147483648)
	{
		ft_putlstr("-2147483648", n);
		return ;
	}
	if (nb < 0)
	{
		ft_putlchar('-', n);
		nb = nb * -1;
	}
	if (nb >= 10)
		ft_putlnbr(nb / 10, n);
	ft_putlchar((nb % 10) + '0', n);
}
